# 문자열형 변수 var 생성
var = '파이썬, 클래스와 객체'
print('var       \t: ', var)
print('id(var)   \t: ', id(var))
print('type(var) \t: ', type(var))

# str, str의 타입, str의 식별자 확인
print('\nstr       \t: ', str)
print('type(str) \t: ', type(str))
print('id(str)   \t: ', id(str))

# var 지역변수 __class__ 값 확인
print('\nvar.__class__ : ', var.__class__)

var2 = var.replace('파이썬', 'Python')
print('\nvar2    \t: ', var2)